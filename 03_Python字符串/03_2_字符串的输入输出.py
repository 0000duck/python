# 字符串格式化符号   %。

# 用 %变量名所代表的值  替换  %s 等字符串格式化符号

name = 'xiaoming'
position = '讲师'
address = '上海市浦东新区'

print('----------------------')
print("姓名：%s"%name)
print("职位：%s"%position)
print("住址：%s"%address)
print('----------------------')


